export const getIconFromFSMActions = (action: string, disabled: boolean): IconType => {
	switch (action) {
		case '/instantiate':
			return {
				type: Icons.bolt,
				tooltip: 'Activate pipeline',
				disabled: disabled,
				dispatchEventName: 'activate'
			};
		case '/commit':
			return {
				type: Icons.lockClosed,
				tooltip: 'commit',
				disabled: disabled,
				fill: 'none',
				strokeWidth: 2,
				dispatchEventName: 'commit'
			};
		case '/reset':
			return {
				type: Icons.refresh,
				tooltip: 'reset',
				disabled: disabled,
				dispatchEventName: 'reset'
			};
		default:
			return {
				type: Icons.default,
				disabled: true
			};
	}
};
